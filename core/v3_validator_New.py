def _check_text_layer_always_hidden(section: dict) -> List[V3ValidatorError]:
    """V3 Rule: text_layer must ALWAYS be 'hide'. No text overlays."""
    errors = []
    sid = section.get("section_id", 0)
    for i, seg in enumerate(_get_segments(section)):
        dd         = seg.get("display_directives", {})
        text_layer = dd.get("text_layer", "")
        if text_layer == "show":
            errors.append(V3ValidatorError(
                "v3_text_layer_show_forbidden", sid,
                f"Segment {i}: text_layer='show' is FORBIDDEN in V3. "
                f"All content is delivered via avatar + visual. Set text_layer='hide'.",
            ))
    return errors


def _check_renderer_valid(section: dict) -> List[V3ValidatorError]:
    """Renderer must be a known V3 value."""
    errors = []
    sid      = section.get("section_id", 0)
    renderer = section.get("renderer", "")
    if renderer and renderer not in VALID_RENDERERS:
        errors.append(V3ValidatorError(
            "v3_invalid_renderer", sid,
            f"renderer='{renderer}' is not valid. Must be one of: {sorted(VALID_RENDERERS)}",
        ))
    # FIX 3: explicit error for deprecated renderers that may sneak through
    if renderer in ("threejs", "wan", "wan_video", "video"):
        errors.append(V3ValidatorError(
            "v3_deprecated_renderer", sid,
            f"renderer='{renderer}' is deprecated/invalid in V3. "
            f"Use: manim, image_to_video, text_to_video, none, per_question.",
        ))
    return errors


def _check_manim_spec(section: dict) -> List[V3ValidatorError]:
    """manim sections MUST have render_spec with manim_beats[] array."""
    errors = []
    sid              = section.get("section_id", 0)
    section_renderer = section.get("renderer", "")

    if section_renderer != "manim":
        return errors

    render_spec = section.get("render_spec", {})
    if not render_spec:
        errors.append(V3ValidatorError(
            "v3_manim_missing_render_spec", sid,
            "renderer='manim' but section has no render_spec.",
        ))
        return errors

    # FIX 4: render_spec.renderer must NOT exist in V3
    if "renderer" in render_spec:
        errors.append(V3ValidatorError(
            "v3_render_spec_renderer_forbidden", sid,
            f"render_spec.renderer='{render_spec['renderer']}' must NOT exist in V3. "
            f"The renderer field lives on section, not render_spec.",
        ))

    renderer_reason = render_spec.get("renderer_reason", "").strip()
    if not renderer_reason:
        errors.append(V3ValidatorError(
            "v3_renderer_reason_missing", sid,
            "render_spec.renderer_reason is REQUIRED — explain why manim was chosen.",
        ))

    manim_beats = render_spec.get("manim_beats", [])
    if not manim_beats:
        errors.append(V3ValidatorError(
            "v3_manim_beats_missing", sid,
            "render_spec.manim_beats is missing or empty — required for Manim generation.",
        ))
        return errors

    for i, beat in enumerate(manim_beats):
        spec = beat.get("manim_scene_spec", "")
        if not spec:
            errors.append(V3ValidatorError(
                "v3_manim_spec_missing", sid,
                f"render_spec.manim_beats[{i}].manim_scene_spec is missing.",
            ))
        elif _count_words(spec) < MANIM_SPEC_MIN_WORDS:
            wc = _count_words(spec)
            errors.append(V3ValidatorError(
                "v3_manim_spec_too_short", sid,
                f"render_spec.manim_beats[{i}] is {wc} words — minimum {MANIM_SPEC_MIN_WORDS}. "
                f"Add explicit timing, colours, labels, and object positions.",
            ))
        else:
            vague = _check_vague(spec)
            if vague:
                errors.append(V3ValidatorError(
                    "v3_vague_manim_spec", sid,
                    f"render_spec.manim_beats[{i}] contains vague language: {vague}",
                ))

    if not render_spec.get("total_duration_seconds"):
        errors.append(V3ValidatorError(
            "v3_total_duration_missing", sid,
            "render_spec.total_duration_seconds not set — Manim generator needs this.",
        ))

    return errors


def _check_image_to_video_spec(section: dict) -> List[V3ValidatorError]:
    """FIX 8: image_to_video sections must have valid image_to_video_beats with word counts."""
    errors = []
    sid              = section.get("section_id", 0)
    section_renderer = section.get("renderer", "")

    if section_renderer != "image_to_video":
        return errors

    render_spec = section.get("render_spec", {})
    if not render_spec:
        errors.append(V3ValidatorError(
            "v3_itv_missing_render_spec", sid,
            "renderer='image_to_video' but section has no render_spec.",
        ))
        return errors

    # FIX 4 also applies here
    if "renderer" in render_spec:
        errors.append(V3ValidatorError(
            "v3_render_spec_renderer_forbidden", sid,
            f"render_spec.renderer='{render_spec['renderer']}' must NOT exist in V3.",
        ))

    renderer_reason = render_spec.get("renderer_reason", "").strip()
    if not renderer_reason:
        errors.append(V3ValidatorError(
            "v3_renderer_reason_missing", sid,
            "render_spec.renderer_reason is REQUIRED for image_to_video sections.",
        ))

    beats = render_spec.get("image_to_video_beats", [])
    if not beats:
        errors.append(V3ValidatorError(
            "v3_itv_beats_missing", sid,
            "render_spec.image_to_video_beats is missing or empty.",
        ))
        return errors

    for i, beat in enumerate(beats):
        bid = beat.get("beat_id", i)

        # image_prompt_start — 100+ words
        ips = beat.get("image_prompt_start", "")
        if not ips:
            errors.append(V3ValidatorError(
                "v3_itv_image_prompt_start_missing", sid,
                f"beat[{i}] (id={bid}): image_prompt_start is missing.",
            ))
        elif _count_words(ips) < IMAGE_PROMPT_MIN_WORDS:
            errors.append(V3ValidatorError(
                "v3_itv_image_prompt_start_too_short", sid,
                f"beat[{i}] (id={bid}): image_prompt_start is {_count_words(ips)} words — "
                f"minimum {IMAGE_PROMPT_MIN_WORDS}.",
            ))

        # image_prompt_end — 100+ words
        ipe = beat.get("image_prompt_end", "")
        if not ipe:
            errors.append(V3ValidatorError(
                "v3_itv_image_prompt_end_missing", sid,
                f"beat[{i}] (id={bid}): image_prompt_end is missing.",
            ))
        elif _count_words(ipe) < IMAGE_PROMPT_MIN_WORDS:
            errors.append(V3ValidatorError(
                "v3_itv_image_prompt_end_too_short", sid,
                f"beat[{i}] (id={bid}): image_prompt_end is {_count_words(ipe)} words — "
                f"minimum {IMAGE_PROMPT_MIN_WORDS}.",
            ))

        # video_prompt — 80+ words
        vp = beat.get("video_prompt", "")
        if not vp:
            errors.append(V3ValidatorError(
                "v3_itv_video_prompt_missing", sid,
                f"beat[{i}] (id={bid}): video_prompt is missing.",
            ))
        elif _count_words(vp) < VIDEO_PROMPT_MIN_WORDS:
            errors.append(V3ValidatorError(
                "v3_itv_video_prompt_too_short", sid,
                f"beat[{i}] (id={bid}): video_prompt is {_count_words(vp)} words — "
                f"minimum {VIDEO_PROMPT_MIN_WORDS}.",
            ))

        # duration — max 15s
        dur = beat.get("duration")
        if dur and dur > 15:
            errors.append(V3ValidatorError(
                "v3_itv_beat_duration_exceeds_15s", sid,
                f"beat[{i}] (id={bid}): duration={dur}s exceeds maximum 15s.",
            ))

    if not render_spec.get("total_duration_seconds"):
        errors.append(V3ValidatorError(
            "v3_total_duration_missing", sid,
            "render_spec.total_duration_seconds not set.",
        ))

    return errors



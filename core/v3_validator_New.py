import re
from typing import List, Tuple, Dict, Any

CONTENT_MIN_WORDS     = 150
EXAMPLE_MIN_WORDS     = 100
MANIM_SPEC_MIN_WORDS  = 100   # FIX 1: was 30, prompt requires 100+
IMAGE_PROMPT_MIN_WORDS = 100  # image_prompt_start / image_prompt_end
VIDEO_PROMPT_MIN_WORDS = 80   # video_prompt

# FIX 2: memory is NOT optional in V3
REQUIRED_SECTION_TYPES = ["intro", "summary", "memory", "recap"]

VAGUE_PHRASES = [
    "appropriate animation", "suitable visual", "relevant content",
    "necessary elements", "various objects", "etc", "and so on",
    "properly animated", "correctly displayed", "accordingly",
    "as needed", "as required", "generic visual", "typical animation",
    "standard display", "some kind of", "some sort of", "a type of",
]

# FIX 3: removed deprecated/non-V3 renderers (threejs, wan, wan_video, video)
VALID_RENDERERS = {
    "manim",
    "image_to_video",
    "text_to_video",
    "infographic",
    "image",
    "none",
    "per_question",
}

# Valid renderers for explanation_visual
VALID_EV_RENDERERS = {
    "manim", "image_to_video", "text_to_video", "infographic", "image",
}


class V3ValidatorError(Exception):
    """A single V3 validation failure."""

    def __init__(self, condition: str, section_id, details: str):
        self.condition  = condition
        self.section_id = section_id
        self.details    = details
        super().__init__(f"[{condition}] Section {section_id}: {details}")


def _count_words(text: str) -> int:
    return len(text.split()) if text else 0


def _check_vague(text: str) -> List[str]:
    lower = text.lower()
    return [p for p in VAGUE_PHRASES if p in lower]


def _get_narration_text(section: dict) -> str:
    n = section.get("narration", "")
    if isinstance(n, dict):
        return n.get("full_text", "")
    return str(n) if n else ""


def _get_segments(section: dict) -> List[dict]:
    n = section.get("narration", {})
    if isinstance(n, dict):
        return n.get("segments", [])
    return []



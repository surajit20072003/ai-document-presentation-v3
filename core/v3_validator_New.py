def _check_timing_contract(section: dict) -> List[V3ValidatorError]:
    """FIX 9: end_seconds must equal start_seconds + duration_seconds exactly."""
    errors = []
    sid          = section.get("section_id", 0)
    section_type = section.get("section_type", "")
    if section_type not in ("content", "example"):
        return errors

    prev_end = None
    for seg in _get_segments(section):
        seg_id = seg.get("segment_id", "?")
        s0     = seg.get("start_seconds")
        e0     = seg.get("end_seconds")
        d0     = seg.get("duration_seconds")

        # Timing contract: end = start + duration
        if all(v is not None for v in [s0, e0, d0]):
            expected_end = round(s0 + d0, 3)
            if abs(expected_end - e0) > 0.15:
                errors.append(V3ValidatorError(
                    "v3_timing_contract_broken", sid,
                    f"Segment {seg_id}: start={s0} + duration={d0} = {expected_end} "
                    f"but end_seconds={e0}. "
                    f"Do NOT round end_seconds independently — derive it from start+duration.",
                ))

        # Contiguous: no gap between previous end and this start
        if prev_end is not None and s0 is not None:
            if abs(s0 - prev_end) > 0.15:
                errors.append(V3ValidatorError(
                    "v3_timing_gap_between_segments", sid,
                    f"Segment {seg_id}: start_seconds={s0} but previous segment "
                    f"ended at {prev_end}. Gap of {round(s0-prev_end,3)}s — segments must be contiguous.",
                ))

        if e0 is not None:
            prev_end = e0

    return errors


def _check_visual_beats(section: dict) -> List[V3ValidatorError]:
    """FIX 11: visual_type=image beats must have image_source set."""
    errors = []
    sid = section.get("section_id", 0)
    for vb in section.get("visual_beats", []):
        if vb.get("visual_type") == "image" and not vb.get("image_source"):
            errors.append(V3ValidatorError(
                "v3_image_beat_missing_image_source", sid,
                f"visual_beat id={vb.get('beat_id','?')} has visual_type='image' "
                f"but image_source is null. Must reference the source filename.",
            ))
    return errors



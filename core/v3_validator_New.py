def _check_explanation_visual(ev: dict, sid, context: str) -> List[V3ValidatorError]:
    """Shared check for explanation_visual on understanding_quiz and standalone quiz."""
    errors = []

    if not ev:
        errors.append(V3ValidatorError(
            "v3_explanation_visual_missing", sid,
            f"{context}: explanation_visual is REQUIRED.",
        ))
        return errors

    ev_renderer = ev.get("renderer", "")
    if not ev_renderer:
        errors.append(V3ValidatorError(
            "v3_explanation_visual_renderer_missing", sid,
            f"{context}: explanation_visual.renderer is missing.",
        ))
    elif ev_renderer not in VALID_EV_RENDERERS:
        errors.append(V3ValidatorError(
            "v3_explanation_visual_renderer_invalid", sid,
            f"{context}: explanation_visual.renderer='{ev_renderer}' is invalid. "
            f"Must be one of: {sorted(VALID_EV_RENDERERS)}",
        ))

    if not ev.get("renderer_reason", "").strip():
        errors.append(V3ValidatorError(
            "v3_explanation_visual_renderer_reason_missing", sid,
            f"{context}: explanation_visual.renderer_reason is REQUIRED.",
        ))

    if not ev.get("display_text", "").strip():
        errors.append(V3ValidatorError(
            "v3_explanation_visual_display_text_missing", sid,
            f"{context}: explanation_visual.display_text is REQUIRED.",
        ))

    if not ev.get("duration_seconds"):
        errors.append(V3ValidatorError(
            "v3_explanation_visual_duration_missing", sid,
            f"{context}: explanation_visual.duration_seconds is REQUIRED.",
        ))

    # Renderer-specific spec checks
    if ev_renderer == "manim":
        ev_mb = ev.get("manim_beats", [])
        if not ev_mb:
            # fallback: old-style manim_scene_spec string
            mss = ev.get("manim_scene_spec", "")
            if not mss:
                errors.append(V3ValidatorError(
                    "v3_explanation_visual_manim_spec_missing", sid,
                    f"{context}: explanation_visual.renderer=manim but "
                    f"manim_beats[] is missing.",
                ))
            elif _count_words(mss) < MANIM_SPEC_MIN_WORDS:
                errors.append(V3ValidatorError(
                    "v3_explanation_visual_manim_spec_too_short", sid,
                    f"{context}: explanation_visual.manim_scene_spec is "
                    f"{_count_words(mss)} words — minimum {MANIM_SPEC_MIN_WORDS}.",
                ))
        else:
            for i, b in enumerate(ev_mb):
                wc = _count_words(b.get("manim_scene_spec", ""))
                if wc < MANIM_SPEC_MIN_WORDS:
                    errors.append(V3ValidatorError(
                        "v3_explanation_visual_manim_beat_too_short", sid,
                        f"{context}: explanation_visual.manim_beats[{i}] is "
                        f"{wc} words — minimum {MANIM_SPEC_MIN_WORDS}.",
                    ))

    elif ev_renderer == "image_to_video":
        ev_itv = ev.get("image_to_video_beats", [])
        if not ev_itv:
            errors.append(V3ValidatorError(
                "v3_explanation_visual_itv_beats_missing", sid,
                f"{context}: explanation_visual.renderer=image_to_video but "
                f"image_to_video_beats[] is missing.",
            ))
        else:
            for i, b in enumerate(ev_itv):
                for field, minw in [("image_prompt_start", IMAGE_PROMPT_MIN_WORDS),
                                    ("image_prompt_end",   IMAGE_PROMPT_MIN_WORDS),
                                    ("video_prompt",       VIDEO_PROMPT_MIN_WORDS)]:
                    wc = _count_words(b.get(field, ""))
                    if wc < minw:
                        errors.append(V3ValidatorError(
                            "v3_explanation_visual_itv_prompt_too_short", sid,
                            f"{context}: explanation_visual.image_to_video_beats[{i}]."
                            f"{field} is {wc} words — minimum {minw}.",
                        ))

    elif ev_renderer == "text_to_video":
        vp = ev.get("video_prompt", "")
        if not vp:
            errors.append(V3ValidatorError(
                "v3_explanation_visual_video_prompt_missing", sid,
                f"{context}: explanation_visual.renderer=text_to_video but "
                f"video_prompt is missing.",
            ))
        elif _count_words(vp) < VIDEO_PROMPT_MIN_WORDS:
            errors.append(V3ValidatorError(
                "v3_explanation_visual_video_prompt_too_short", sid,
                f"{context}: explanation_visual.video_prompt is "
                f"{_count_words(vp)} words — minimum {VIDEO_PROMPT_MIN_WORDS}.",
            ))

    return errors


def _check_understanding_quiz(section: dict) -> List[V3ValidatorError]:
    """content/example sections MUST have a complete understanding_quiz."""
    errors = []
    sid          = section.get("section_id", 0)
    section_type = section.get("section_type", "")
    if section_type not in ("content", "example"):
        return errors

    quiz = section.get("understanding_quiz", {})
    if not quiz:
        errors.append(V3ValidatorError(
            "v3_understanding_quiz_missing", sid,
            "content/example sections MUST include understanding_quiz. No skip condition.",
        ))
        return errors

    if not quiz.get("question"):
        errors.append(V3ValidatorError(
            "v3_understanding_quiz_missing_question", sid,
            "understanding_quiz.question is empty.",
        ))

    opts = quiz.get("options", {})
    if not opts:
        errors.append(V3ValidatorError(
            "v3_understanding_quiz_missing_options", sid,
            "understanding_quiz.options is empty.",
        ))
    elif len(opts) < 4:
        errors.append(V3ValidatorError(
            "v3_understanding_quiz_too_few_options", sid,
            f"understanding_quiz must have 4 options (A/B/C/D) — found {len(opts)}: {list(opts.keys())}",
        ))

    if not quiz.get("correct"):
        errors.append(V3ValidatorError(
            "v3_understanding_quiz_missing_answer", sid,
            "understanding_quiz.correct (answer key) is missing.",
        ))

    if not quiz.get("explanation"):
        errors.append(V3ValidatorError(
            "v3_understanding_quiz_missing_explanation", sid,
            "understanding_quiz.explanation is missing.",
        ))

    # Narration scripts — FIX 5: now checks all 4 scripts
    narr = quiz.get("narration", {})
    if not narr:
        errors.append(V3ValidatorError(
            "v3_understanding_quiz_missing_narration", sid,
            "understanding_quiz.narration is missing. "
            "Required: question_script, correct_script, wrong_script, explanation_script.",
        ))
    else:
        for script_key in ("question_script", "correct_script",
                           "wrong_script", "explanation_script"):   # FIX 5
            if not narr.get(script_key):
                errors.append(V3ValidatorError(
                    "v3_understanding_quiz_missing_narration_script", sid,
                    f"understanding_quiz.narration.{script_key} is missing or empty.",
                ))

        # FIX 13: option_reveal_seconds must have exactly 4 values
        ors = narr.get("option_reveal_seconds", [])
        if not ors:
            errors.append(V3ValidatorError(
                "v3_understanding_quiz_missing_option_reveal_seconds", sid,
                "understanding_quiz.narration.option_reveal_seconds is missing. "
                "Must have exactly 4 float values (one per A/B/C/D button).",
            ))
        elif len(ors) != 4:
            errors.append(V3ValidatorError(
                "v3_understanding_quiz_option_reveal_seconds_wrong_count", sid,
                f"understanding_quiz.narration.option_reveal_seconds has {len(ors)} values — "
                f"must have exactly 4 (one per A/B/C/D button).",
            ))

    # FIX 6: avatar_clips — all 4 keys required
    clips = quiz.get("avatar_clips", {})
    if not clips:
        errors.append(V3ValidatorError(
            "v3_understanding_quiz_avatar_clips_missing", sid,
            "understanding_quiz.avatar_clips is missing. "
            "Required keys: question, correct, wrong, explanation.",
        ))
    else:
        for clip_key in ("question", "correct", "wrong", "explanation"):
            if not clips.get(clip_key):
                errors.append(V3ValidatorError(
                    "v3_understanding_quiz_avatar_clip_missing", sid,
                    f"understanding_quiz.avatar_clips.{clip_key} is missing. "
                    f"All 4 clips are required for the player interaction flow.",
                ))

    # FIX 7: explanation_visual — full check
    ev = quiz.get("explanation_visual", {})
    errors.extend(_check_explanation_visual(ev, sid, "understanding_quiz"))

    return errors


def _check_quiz_section(section: dict) -> List[V3ValidatorError]:
    """Standalone quiz sections: each question needs 4 scripts, 4 clips, explanation_visual."""
    errors = []
    sid              = section.get("section_id", 0)
    section_type     = section.get("section_type", "")
    section_renderer = section.get("renderer", "")

    if section_type != "quiz" and section_renderer != "per_question":
        return errors

    questions = section.get("questions", [])
    if not questions:
        errors.append(V3ValidatorError(
            "v3_quiz_no_questions", sid, "Quiz section has no questions array.",
        ))
        return errors

    for i, q in enumerate(questions):
        qid  = q.get("question_id", i)
        narr = q.get("narration", {})

        # 4 scripts
        for script_key in ("question_script", "correct_script",
                           "wrong_script", "explanation_script"):
            if not narr.get(script_key):
                errors.append(V3ValidatorError(
                    "v3_quiz_missing_narration_script", sid,
                    f"Question {i} (id={qid}) missing narration.{script_key}.",
                ))

        # options
        opts = q.get("options", {})
        if len(opts) < 4:
            errors.append(V3ValidatorError(
                "v3_quiz_insufficient_options", sid,
                f"Question {i} has {len(opts)} options — minimum 4 (A/B/C/D).",
            ))

        if not q.get("correct_option"):
            errors.append(V3ValidatorError(
                "v3_quiz_missing_correct_option", sid,
                f"Question {i} (id={qid}) has no correct_option.",
            ))

        # explanation_visual — full check
        ev = q.get("explanation_visual", {})
        errors.extend(_check_explanation_visual(ev, sid, f"Quiz question {i} (id={qid})"))

    return errors


def _check_narration_length(section: dict) -> List[V3ValidatorError]:
    """content/example must meet minimum narration word counts."""
    errors = []
    sid          = section.get("section_id", 0)
    section_type = section.get("section_type", "")
    if section_type not in ("content", "example"):
        return errors
    text    = _get_narration_text(section)
    wc      = _count_words(text)
    minimum = CONTENT_MIN_WORDS if section_type == "content" else EXAMPLE_MIN_WORDS
    if wc < minimum:
        errors.append(V3ValidatorError(
            "v3_narration_below_minimum", sid,
            f"{section_type} section has {wc} words — minimum is {minimum}.",
        ))
    return errors



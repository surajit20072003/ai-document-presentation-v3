def validate_presentation_v3(presentation: dict) -> Tuple[bool, List[V3ValidatorError]]:
    """
    Validate entire V3 presentation.json.

    Returns:
        (is_valid: bool, errors: List[V3ValidatorError])
    """
    all_errors: List[V3ValidatorError] = []
    sections = presentation.get("sections", [])

    # Required section types — FIX 2: memory is required
    section_types = {s.get("section_type") for s in sections}
    for required in REQUIRED_SECTION_TYPES:
        if required not in section_types:
            all_errors.append(V3ValidatorError(
                f"v3_missing_{required}_section", 0,
                f"Presentation is missing required '{required}' section.",
            ))

    # Per-section checks
    for section in sections:
        all_errors.extend(validate_section_v3(section))

    return len(all_errors) == 0, all_errors


def format_v3_report(errors: List[V3ValidatorError]) -> str:
    """Format V3 validation errors into a readable report."""
    if not errors:
        return "✅ V3 Validation passed — no issues found."

    lines = [
        "=" * 60,
        "V3 VALIDATOR REPORT",
        "=" * 60,
        f"Total failures: {len(errors)}",
        "",
    ]
    by_condition: Dict[str, List[V3ValidatorError]] = {}
    for e in errors:
        by_condition.setdefault(e.condition, []).append(e)

    for cond, errs in by_condition.items():
        lines.append(f"\n[{cond}] — {len(errs)} occurrence(s)")
        for e in errs:
            lines.append(f"  Section {e.section_id}: {e.details}")

    lines.append("\n" + "=" * 60)
    return "\n".join(lines)



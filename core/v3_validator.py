def validate_section_v3(section: dict) -> List[V3ValidatorError]:
    """Run all V3 checks on a single section."""
    errors = []
    errors.extend(_check_text_layer_always_hidden(section))
    errors.extend(_check_renderer_valid(section))
    errors.extend(_check_manim_spec(section))
    errors.extend(_check_segment_duration(section))
    errors.extend(_check_understanding_quiz(section))
    errors.extend(_check_quiz_section(section))
    errors.extend(_check_narration_length(section))
    errors.extend(_check_display_directives(section))
    return errors



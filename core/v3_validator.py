"""
V3 Hard Fail Validator — V3.0 (Manim Edition)

Validates presentation.json generated by V3 Director (Partition Mode).
V3 rules:

V3 CHANGES (Manim replaces Three.js):
 - text_layer: ALWAYS "hide" (no text overlays in V3)
 - renderer: "manim" is now the primary visual renderer
 - manim sections MUST have render_spec with manim_scene_spec (30+ words)
 - segment_duration_seconds REQUIRED on all content/example sections
 - understanding_quiz REQUIRED on all content/example sections
 - quiz sections: each question must have 3 narration scripts
   (question_script, correct_script, wrong_script) for 3 avatar clips
 - visual_beats: visual_type "manim" is valid
"""

import re
from typing import List, Tuple, Dict, Any

CONTENT_MIN_WORDS = 150
EXAMPLE_MIN_WORDS = 100
MANIM_SPEC_MIN_WORDS = 30  # Relaxed from 100 — director prompt tuning in progress

REQUIRED_SECTION_TYPES = ["intro", "summary", "recap"]  # memory optional in V3

VAGUE_PHRASES = [
    "appropriate animation",
    "suitable visual",
    "relevant content",
    "necessary elements",
    "various objects",
    "etc",
    "and so on",
    "properly animated",
    "correctly displayed",
    "accordingly",
    "as needed",
    "as required",
    "generic visual",
    "typical animation",
    "standard display",
    "some kind of",
    "some sort of",
    "a type of",
]

VALID_RENDERERS = {"manim", "video", "none", "wan", "wan_video"}


class V3ValidatorError(Exception):
    """A single V3 validation failure."""

    def __init__(self, condition: str, section_id, details: str):
        self.condition = condition
        self.section_id = section_id
        self.details = details
        super().__init__(f"[{condition}] Section {section_id}: {details}")


def _count_words(text: str) -> int:
    return len(text.split()) if text else 0


def _check_vague(text: str) -> List[str]:
    lower = text.lower()
    return [p for p in VAGUE_PHRASES if p in lower]


def _get_narration_text(section: dict) -> str:
    n = section.get("narration", "")
    if isinstance(n, dict):
        return n.get("full_text", "")
    return str(n) if n else ""


def _get_segments(section: dict) -> List[dict]:
    n = section.get("narration", {})
    if isinstance(n, dict):
        return n.get("segments", [])
    return []



if __name__ == "__main__":
    import json, sys

    path = sys.argv[1] if len(sys.argv) > 1 else "presentation.json"
    with open(path) as f:
        data = json.load(f)
    ok, errors = validate_presentation_v3(data)
    print(format_v3_report(errors))
    sys.exit(0 if ok else 1)
